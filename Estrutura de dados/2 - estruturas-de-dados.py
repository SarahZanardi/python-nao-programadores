# Definindo os anos de nascimento e formatura
ano_nascimento = 1989
ano_formatura = 2010

# Calcular e imprimir a idade de Gerlaine no ano da formatura
print(ano_formatura - ano_nascimento)  # A idade é a diferença entre o ano de formatura e o ano de nascimento

# Comparações com operadores relacionais:
# Verifica se o ano de formatura é maior que o ano de nascimento
print(ano_formatura > ano_nascimento)

# Verifica se o ano de formatura é menor ou igual ao ano de nascimento
print(ano_formatura <= ano_nascimento)

# Verifica se o ano de formatura é igual ao ano de nascimento
print(ano_formatura == ano_nascimento)

